import mongoose from 'mongoose';

const contactFrom = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
});

export const contactData = mongoose.model('contact', contactFrom);

